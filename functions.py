import numpy as np

def generate_sentence_vectors(tokenized_sentences: list, word_embeddings: dict):
    """
    Generate sentence vectors from tokenized sentences using word embeddings.

    Args:
    - tokenized_sentences (list): List of tokenized sentences.
    - word_embeddings (dict): Dictionary containing word embeddings.

    Returns:
    - sentence_vectors (list): List of sentence vectors.
    """
    sentence_vectors = []

    # Handle case when tokenized_sentences is empty
    if not tokenized_sentences:
        return [np.zeros_like(word_embeddings['a'])]

    # Iterate through each tokenized sentence
    for sentence in tokenized_sentences:
        vector_sum = np.zeros_like(word_embeddings['a'])
        word_count = 0

        # Calculate the sum of word vectors in the sentence
        for word in sentence:
            if word in word_embeddings:
                vector_sum += word_embeddings[word]
                word_count += 1

        # Calculate the average sentence vector
        if word_count > 0:
            average_vector = vector_sum / word_count
        else:
            average_vector = np.zeros_like(word_embeddings['a'])

        sentence_vectors.append(average_vector)

    return sentence_vectors

def batch_generator(tweets=[], hashtags=[], labels=[], batch_size=1000):
    """
    Generate batches of data from provided lists of tweets, hashtags, and labels.

    Args:
    - tweets (list): List of tweets.
    - hashtags (list): List of hashtags.
    - labels (list): List of labels.
    - batch_size (int): Size of each batch.

    Yields:
    - tuple: Batch of tweets, hashtags, and labels.
    """
    # Iterate through the data lists in batch sizes
    for i in range(0, len(tweets), batch_size):
        # Yield a batch of tweets, hashtags, and labels
        yield tweets[i:i+batch_size], hashtags[i:i+batch_size], labels[i:i+batch_size]