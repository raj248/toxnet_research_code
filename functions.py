def to_device(data, device):
    """
    Move tensor(s) to the chosen device.

    Args:
    - data: Tensor or list/tuple of tensors to move to the device.
    - device (torch.device): Target device for the data.

    Returns:
    - Tensor or list/tuple of tensors: Data moved to the chosen device.
    """
    if isinstance(data, (list, tuple)):
        return [to_device(x, device) for x in data]
    return data.to(device, non_blocking=True)

