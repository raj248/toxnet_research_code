# Load the English language model
nlp = spacy.load('en_core_web_sm')

def extract_aspect_words(sentence):
    """
    Extract aspect words (nouns, proper nouns, and adjectives) from a given sentence using spaCy.

    Args:
    - sentence (str): The input sentence to extract aspect words from.

    Returns:
    - aspect_words (list): List of aspect words extracted from the sentence.
    """
    # Parse the sentence using spaCy
    doc = nlp(sentence)

    # Initialize a list to store aspect words
    aspect_words = []

    # Iterate through each token in the parsed sentence
    for token in doc:
        # Check if the token is a noun, proper noun, or adjective
        if token.pos_ in ['NOUN', 'PROPN', 'ADJ']:
            # Add the token to the list of aspect words
            aspect_words.append(token.text)

    return aspect_words



