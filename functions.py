import re
import wordninja
import nltk
from nltk.corpus import stopwords
from nltk.stem import WordNetLemmatizer

nltk.download('wordnet')
nltk.download('stopwords')

tokenizer = nltk.RegexpTokenizer(r"\w+")
lemmatizer = WordNetLemmatizer()
stop_words = set(stopwords.words('english'))

def split_word_and_filter(concatenated_word: str, embeddings: dict) -> list:
    """
    Split a concatenated word into meaningful components and filter out
    words that do not exist in the embeddings.

    Args:
    - concatenated_word (str): The concatenated word to be split.
    - embeddings (dict): Dictionary containing word embeddings.

    Returns:
    - list: A list of split words that exist in the embeddings.
    """
    split_words = wordninja.split(concatenated_word)
    valid_words = [word for word in split_words if word in embeddings]
    return valid_words

def preprocess_text_and_tokenize(text: str, embeddings: dict) -> list:
    """
    Preprocess a text message and tokenize it based on embeddings.

    Args:
    - text (str): The input text message to preprocess and tokenize.
    - embeddings (dict): Dictionary containing word embeddings.

    Returns:
    - list: A list of useful tokens extracted from the preprocessed text.
    """
    cleaned_text = text.lower()
    cleaned_text = re.sub(r'@\w+\b|#\w+\b|https?://\S+', '', cleaned_text)
    cleaned_text = re.sub(r'\b(rt)\b', '', cleaned_text, flags=re.IGNORECASE)
    cleaned_text = re.sub(r'[^a-zA-Z]', ' ', cleaned_text)
    cleaned_text = re.sub(r'\b\w{1}\b', '', cleaned_text)
    cleaned_text = re.sub(r'\s+', ' ', cleaned_text).strip()
    tokens = tokenizer.tokenize(cleaned_text)
    lowercased_tokens = [t.lower() for t in tokens]
    lemmatized_tokens = [lemmatizer.lemmatize(t) for t in lowercased_tokens]
    filtered_tokens = [t for t in lemmatized_tokens if t not in stop_words]

    useful_tokens = []
    for t in filtered_tokens:
        if t in embeddings:
            useful_tokens.append(t)
        else:
            useful_tokens.extend(split_word_and_filter(t, embeddings))

    return useful_tokens

