def process_tweet_hashtags(tweet_series, embedding_dictionary):
    """
    Process hashtags from a series of tweets, including splitting concatenated hashtags into meaningful words.

    Args:
    - tweet_series (pandas.Series): Series containing tweets.
    - embedding_dictionary (dict): Dictionary containing word embeddings.

    Returns:
    - list: A list of processed hashtags.
    """
    def get_hashtags(tweet):
        hashtags = re.findall(r'#(\w+)', tweet)
        return hashtags

    def remove_numeric_hashtags(hashtags):
        return [tag for tag in hashtags if not tag.isdigit()]

    def split_concatenated_hashtags_to_words(concatenated_words):
        words_to_split = [word for word in concatenated_words if word not in embedding_dictionary.keys()]
        split_words_list = [wordninja.split(str(word)) for word in words_to_split]
        return split_words_list

    def remove_empty_strings(lst):
        return [word for word in lst if word != "'"]

    def iterate(item):
        if(len(item)>0 and type(item[0])!=list):
          return item
        _item = []
        for i in item:
          _item.extend(iterate(i))
        return _item

    hashtags = tweet_series.apply(get_hashtags)
    hashtags = hashtags.apply(remove_numeric_hashtags)
    hashtags = hashtags.apply(split_concatenated_hashtags_to_words)
    hashtags = hashtags.apply(remove_empty_strings)
    hashtags = [i for i in hashtags]
    hashtags = [iterate(i) for i in hashtags]
    return hashtags

